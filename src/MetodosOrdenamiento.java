    // Método de inserción con errores
    // Error encontrado: La condicion que se encuentra en el while compara de forma incorrecta 
    // y realiza una cantidad de iteraciones incorrectas
    // Correccion: El while debe ejecutarse mientras i >= 0 y en la segunda condicion 
    // el operador de comparacion "<" se cambia por ">" 
    public int[] insercionPrimero(int[] arregloOriginal) {

        int[] arreglo = Arrays.copyOf(arregloOriginal, arregloOriginal.length);
        for (int j = 1; j < arreglo.length; j++) {
            int key = arreglo[j];
            int i = j - 1;

            while (i >= 0 && arreglo[i] > key) {
                arreglo[i + 1] = arreglo[i];
                i--;
            }
            arreglo[i + 1] = key;
        }
        return arreglo;
    }

    // Método de inserción con errores
    // Error encontrado: En el segundo for todas las posiciones del arreglo estan entorno a j 
    // por lo que no realiza la comparacion y el cambio de forma correcta
    // Correccion: En el segundo for se cambia la variable j por la variable i en todas las posiciones
    public int[] insercionSegundo(int[] arregloOriginal) {

        int[] arreglo = Arrays.copyOf(arregloOriginal, arregloOriginal.length);
        for (int j = 1; j < arreglo.length; j++) {
            int actual = arreglo[j];
            int i = j - 1;

            for (; i >= 0 && arreglo[i] > actual; i--) {
                arreglo[i + 1] = arreglo[i];
            }
            arreglo[i + 1] = actual;
        }
        return arreglo;
    }

